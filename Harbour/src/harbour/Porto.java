package harbour;

public class Porto {

	private String nome;
	private int numImbarcazioni;
	private double metriQuadri;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getNumImbarcazioni() {
		return numImbarcazioni;
	}
	public void setNumImbarcazioni(int numImbarcazioni) {
		this.numImbarcazioni = numImbarcazioni;
	}
	public double getMetriQuadri() {
		return metriQuadri;
	}
	public void setMetriQuadri(double metriQuadri) {
		this.metriQuadri = metriQuadri;
	}
	
	
}
